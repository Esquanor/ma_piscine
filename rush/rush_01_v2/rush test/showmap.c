#include <unistd.h>

void	ft_showmap(int map[6][6])
{
	int		c;
	int		l;
	char	n;
	char	s;

	l = 1;
	s = ' ';
	while (l < 5)
	{
		c = 1;
		while (c < 5)
		{
			n = map[l][c] + '0';
			write(1, &n, 1);
			if (c != 4)
				write(1, &s, 1);
			c++;
		}
		write(1, "\n", 1);
		l++;
	}
}
