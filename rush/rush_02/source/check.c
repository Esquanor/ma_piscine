int	ft_nbr_is_valid(char *nbr)
{
	int	i;

	i = 0;
	while (nbr[i])
	{
		if (!((nbr[i] >= '0') && (nbr[i] <= '9')))
			return (0);
		i++;
	}
	if (i < 10)
		return (i);
	if ((i == 10) && (nbr[9] >= '0' && nbr[9] <= '5'))
		return (i);
	return (0);
}
