int	ft_strcmp(char *s1, char *s2)
{
	char	cs1;
	char	cs2;
	int		i;

	i = 1;
	cs1 = *(s1);
	cs2 = *(s2);
	while (cs1 == cs2 && cs1 != '\0')
	{
		cs1 = *(s1 + i);
		cs2 = *(s2 + i);
		i++;
	}	
	if ((cs1 - cs2) == 0)
		return (0);
	else if ((cs1 - cs2) > 0)
		return (1);
	else
		return (-1);
}
